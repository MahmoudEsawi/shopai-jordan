def add_talabat_mart_products():
    """Add comprehensive Talabat Mart Jordan products with full nutritional info"""
    
    db = ProductDatabase('products.db')
    
    # Clear old products
    print("  Clearing old products...")
    import sqlite3
    conn = sqlite3.connect('products.db')
    conn.execute("DELETE FROM products")
    conn.commit()
    conn.close()
    
    print(" Adding Talabat Mart Jordan Products...\n")
    
    added_count = 0
    
    for product in TALABAT_MART_PRODUCTS:
        # Generate ID
        product['id'] = hashlib.md5(
            f"TalabatMart:{product['name']}".encode()
        ).hexdigest()[:16]
        
        product['currency'] = 'JOD'
        product['in_stock'] = True
        product['scraped_at'] = datetime.now().isoformat()
        
        if db.add_product(product):
            added_count += 1
            print(f" [{added_count}/{len(TALABAT_MART_PRODUCTS)}] {product['name']} - {product['price']} JOD")
        else:
            print(f" Failed to add: {product['name']}")
    
    print(f"\n Successfully added {added_count} products!")
    print(f" Total products in database: {db.get_product_count()}")
    print(f" Store: Talabat Mart Jordan")
    print(f" Currency: JOD (Jordanian Dinar)")
    print(f" Products with nutritional info: {added_count}")
    print(f" Healthy products: {sum(1 for p in TALABAT_MART_PRODUCTS if p.get('is_healthy'))}")
    print(f" Gluten-free products: {sum(1 for p in TALABAT_MART_PRODUCTS if p.get('is_gluten_free'))}")
    
    db.close()


